              <h2>CURRENCY & EXCHANGE</h2>

              <p>
                The official currency of Cambodia is the <strong>Khmer Riel (KHR)</strong>. However, the <strong>US Dollar (USD)</strong> is widely accepted and frequently used for higher-value transactions — especially in hotels, restaurants, retail shops, and taxi services.
              </p>

              <p>
                Cambodian banknotes are available in the following denominations: 50, 100, 200, 500, 1,000, 2,000, 5,000, 10,000, 50,000, and 100,000 Riels. It is advisable for tourists to carry some small denominations of Riel for use in local markets and small-scale purchases.
              </p>

              <p>
                Major international cards such as <strong>Visa</strong>, <strong>MasterCard</strong>, <strong>UnionPay (RJB)</strong>, and <strong>Traveler’s Checks</strong> are accepted at most hotels, restaurants, and retail outlets. ATMs and bank branches supporting these services are available in major cities like Phnom Penh, Siem Reap, Sihanoukville, and Battambang.
              </p>

              <p>
                Cambodia operates a relatively open and flexible currency system. Unlike some neighboring countries, tourists enjoy the convenience of using foreign currency freely. The typical exchange rate is approximately <strong>4,000 KHR per 1 USD</strong>, and currency exchange services are available not only at banks but also in markets and retail areas throughout the country.
              </p>

