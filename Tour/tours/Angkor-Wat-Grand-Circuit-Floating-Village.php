        <script>
          (function() {
            const groupLimits = {
              "2-3": {
                price: 805,
                minAdults: 2,
                maxTotal: 3
              },
              "4-6": {
                price: 695,
                minAdults: 4,
                maxTotal: 6
              },
              "7-9": {
                price: 493,
                minAdults: 7,
                maxTotal: 9
              },
              "10-14": {
                price: 433,
                minAdults: 10,
                maxTotal: 14
              },
              "15-19": {
                price: 386,
                minAdults: 15,
                maxTotal: 19
              },
            };

            let currentGroupKey = null;

            const getQty = (type) => parseInt(document.getElementById(`pkg-${type}-qty`).innerText);
            const setQty = (type, val) => document.getElementById(`pkg-${type}-qty`).innerText = val;

            const totalPeople = () => {
              return getQty('adult') + getQty('child12') + getQty('child75') + getQty('child50') + getQty('child0');
            };

            const updateButtonStates = () => {
              const limits = groupLimits[currentGroupKey];
              const adult = getQty('adult');
              const total = totalPeople();
              const atMinAdults = adult === limits.minAdults;
              const maxReached = total >= limits.maxTotal;

              const disableAdd = (id, condition) => {
                document.querySelector(`#pkg-${id}-qty`).parentElement.querySelectorAll("button")[1].disabled = condition;
              };
              const disableSub = (id, condition) => {
                document.querySelector(`#pkg-${id}-qty`).parentElement.querySelectorAll("button")[0].disabled = condition;
              };

              // Adults
              disableAdd("adult", adult >= limits.maxTotal);
              disableSub("adult", adult <= limits.minAdults);

              // Children
              const childTypes = ["child12", "child75", "child50", "child0"];
              childTypes.forEach((type) => {
                const current = getQty(type);
                disableSub(type, current <= 0);
                disableAdd(type,
                  (atMinAdults && total >= limits.minAdults + 1) || maxReached
                );
              });
            };

            window.pkgUI = {
              updateQty(type, delta) {
                const el = document.getElementById(`pkg-${type}-qty`);
                let val = parseInt(el.innerText);
                const limits = groupLimits[currentGroupKey];
                const adult = getQty("adult");
                const total = totalPeople();

                if (type === "adult") {
                  const newAdult = adult + delta;
                  if (newAdult < limits.minAdults || newAdult > limits.maxTotal) return;
                  setQty("adult", newAdult);
                  if (totalPeople() > limits.maxTotal) {
                    // Reset children if exceeding max
                    ["child12", "child75", "child50", "child0"].forEach(type => setQty(type, 0));
                  }
                } else {
                  const newTotal = total + delta;
                  const isMinAdults = adult === limits.minAdults;

                  if (delta > 0 && (newTotal > limits.maxTotal || (isMinAdults && newTotal > limits.minAdults + 1))) return;
                  if (val + delta < 0) return;

                  setQty(type, val + delta);
                }

                this.updateTotal();
                updateButtonStates();
              },

              updateTotal() {
                const base = groupLimits[currentGroupKey].price;

                const adult = getQty("adult");
                const c12 = getQty("child12");
                const c75 = getQty("child75");
                const c50 = getQty("child50");

                const total =
                  (adult + c12) * base +
                  c75 * base * 0.75 +
                  c50 * base * 0.5;

                document.getElementById("pkg-total-price").innerText = total.toFixed(2);
              },

              selectGroup(key) {
                currentGroupKey = key;

                document.getElementById("pkg-group-step").classList.add("pkg-hidden");
                const form = document.getElementById("pkg-form-step");
                form.classList.remove("pkg-hidden");
                form.classList.add("pkg-fade-in");

                const group = groupLimits[key];
                setQty("adult", group.minAdults);
                ["child12", "child75", "child50", "child0"].forEach(type => setQty(type, 0));
                document.getElementById("pkg-adult-price-label").innerText = `$${group.price.toFixed(2)}`;

                this.updateTotal();
                updateButtonStates();
              },
            };
          })();
        </script>

